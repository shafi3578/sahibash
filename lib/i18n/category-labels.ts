function keyFromPath(path?: string | null) {
  if (!path) return "";
  const parts = path.split("/").filter(Boolean);
  return parts[parts.length - 1] ?? "";
}

export function localizeCategoryName(input: {
  locale: AppLocale;
  fallbackName: string;
  slug?: string | null;
  path?: string | null;
}) {
  const key = (input.slug || keyFromPath(input.path) || "").toLowerCase();
  if (!key) return input.fallbackName;
  return CATEGORY_LABELS[input.locale][key]?.name ?? input.fallbackName;
}

export function localizeCategorySubtitle(input: {
  locale: AppLocale;
  fallbackSubtitle?: string | null;
  slug?: string | null;
  path?: string | null;
}) {
  const key = (input.slug || keyFromPath(input.path) || "").toLowerCase();
  if (!key) return input.fallbackSubtitle ?? "";
  return CATEGORY_LABELS[input.locale][key]?.subtitle ?? input.fallbackSubtitle ?? "";
}
