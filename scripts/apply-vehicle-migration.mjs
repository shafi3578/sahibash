import fs from "node:fs";
import path from "node:path";
import { createClient } from "@supabase/supabase-js";

function readEnvValue(text, key) {
  const line = text.split(/\r?\n/).find((l) => l.trim().startsWith(`${key}=`));
  return line ? line.slice(key.length + 1).trim() : "";
}

async function applyMigration() {
  const envPath = path.join(process.cwd(), ".env.local");
  const envText = fs.readFileSync(envPath, "utf8");
  const url = readEnvValue(envText, "NEXT_PUBLIC_SUPABASE_URL");
  const key = readEnvValue(envText, "SUPABASE_SERVICE_ROLE_KEY");
  const sb = createClient(url, key, { auth: { persistSession: false, autoRefreshToken: false } });

  // Apply each DDL statement via Supabase's management API using REST
  const projectRef = new URL(url).hostname.split(".")[0];
  const statements = [
    `create table if not exists public.vehicle_brands (
      id bigint generated by default as identity primary key,
      category_node_id bigint references public.category_nodes(id) on delete set null,
      name text not null,
      slug text not null,
      display_order int not null default 0,
      is_active boolean not null default true,
      created_at timestamptz not null default now(),
      updated_at timestamptz not null default now(),
      constraint vehicle_brands_slug_unique unique (slug)
    )`,
    `create table if not exists public.vehicle_models (
      id bigint generated by default as identity primary key,
      brand_id bigint not null references public.vehicle_brands(id) on delete cascade,
      name text not null,
      slug text not null,
      body_type text,
      doors int,
      seats int,
      display_order int not null default 0,
      is_active boolean not null default true,
      created_at timestamptz not null default now(),
      updated_at timestamptz not null default now(),
      constraint vehicle_models_brand_slug_unique unique (brand_id, slug)
    )`,
    `create table if not exists public.vehicle_generations (
      id bigint generated by default as identity primary key,
      model_id bigint not null references public.vehicle_models(id) on delete cascade,
      name text not null,
      slug text not null,
      year_start int,
      year_end int,
      display_order int not null default 0,
      is_active boolean not null default true,
      created_at timestamptz not null default now(),
      updated_at timestamptz not null default now(),
      constraint vehicle_generations_model_slug_unique unique (model_id, slug)
    )`,
    `create table if not exists public.vehicle_variants (
      id bigint generated by default as identity primary key,
      generation_id bigint not null references public.vehicle_generations(id) on delete cascade,
      name text not null,
      slug text not null,
      fuel_type text,
      transmission text,
      engine_size text,
      drive_type text,
      display_order int not null default 0,
      is_active boolean not null default true,
      created_at timestamptz not null default now(),
      updated_at timestamptz not null default now(),
      constraint vehicle_variants_gen_slug_unique unique (generation_id, slug)
    )`,
    `create table if not exists public.vehicle_specifications (
      id bigint generated by default as identity primary key,
      variant_id bigint not null references public.vehicle_variants(id) on delete cascade,
      spec_key text not null,
      spec_label text not null,
      spec_value text not null,
      is_locked boolean not null default true,
      sort_order int not null default 0,
      created_at timestamptz not null default now(),
      updated_at timestamptz not null default now(),
      constraint vehicle_specifications_variant_key_unique unique (variant_id, spec_key)
    )`,
    `create table if not exists public.vehicle_damage_reports (
      id bigint generated by default as identity primary key,
      listing_id uuid not null references public.listings(id) on delete cascade,
      all_original boolean not null default false,
      notes text,
      created_at timestamptz not null default now(),
      updated_at timestamptz not null default now(),
      constraint vehicle_damage_reports_listing_unique unique (listing_id)
    )`,
    `create table if not exists public.vehicle_damage_parts (
      id bigint generated by default as identity primary key,
      damage_report_id bigint not null references public.vehicle_damage_reports(id) on delete cascade,
      part_key text not null,
      part_label text not null,
      condition text not null default 'original',
      created_at timestamptz not null default now(),
      constraint vehicle_damage_parts_report_part_unique unique (damage_report_id, part_key)
    )`,
    `alter table public.listings add column if not exists vehicle_variant_id bigint references public.vehicle_variants(id) on delete set null`,
    `create index if not exists idx_vehicle_models_brand on public.vehicle_models(brand_id, is_active, display_order)`,
    `create index if not exists idx_vehicle_generations_model on public.vehicle_generations(model_id, is_active, display_order)`,
    `create index if not exists idx_vehicle_variants_gen on public.vehicle_variants(generation_id, is_active, display_order)`,
    `create index if not exists idx_vehicle_specifications_variant on public.vehicle_specifications(variant_id, sort_order)`,
    `create index if not exists idx_vehicle_damage_reports_listing on public.vehicle_damage_reports(listing_id)`,
    `create index if not exists idx_listings_vehicle_variant on public.listings(vehicle_variant_id)`,
  ];

  const mgmtUrl = `https://api.supabase.com/v1/projects/${projectRef}/database/query`;
  const mgmtKey = key;

  for (const stmt of statements) {
    const res = await fetch(mgmtUrl, {
      method: "POST",
      headers: { "Authorization": `Bearer ${mgmtKey}`, "Content-Type": "application/json" },
      body: JSON.stringify({ query: stmt }),
    });

    if (!res.ok) {
      const text = await res.text();
      // 409/already exists is fine
      if (!text.includes("already exists") && !text.includes("42P07") && !text.includes("42701")) {
        console.warn(`  ⚠ ${res.status}: ${text.slice(0, 180)}`);
      } else {
        console.log(`  ✓ (already exists, ok)`);
      }
      continue;
    }

    console.log(`  ✓ OK`);
  }

  // Enable RLS via Supabase
  const tables = [
    "vehicle_brands","vehicle_models","vehicle_generations",
    "vehicle_variants","vehicle_specifications",
    "vehicle_damage_reports","vehicle_damage_parts"
  ];

  for (const table of tables) {
    const { error } = await sb.rpc("", {}).catch(() => ({ error: null }));
    void error;
    // RLS enablement via admin SDK — try the management API
    const res = await fetch(mgmtUrl, {
      method: "POST",
      headers: { "Authorization": `Bearer ${mgmtKey}`, "Content-Type": "application/json" },
      body: JSON.stringify({ query: `alter table public.${table} enable row level security` }),
    });

    const text = await res.text();
    if (!res.ok && !text.includes("already enabled")) {
      console.warn(`  ⚠ rls ${table}: ${text.slice(0, 140)}`);
    }

    // Public read policy
    const policyRes = await fetch(mgmtUrl, {
      method: "POST",
      headers: { "Authorization": `Bearer ${mgmtKey}`, "Content-Type": "application/json" },
      body: JSON.stringify({ query: `do $$ begin if not exists (select 1 from pg_policies where schemaname='public' and tablename='${table}' and policyname='${table}_public_read') then execute 'create policy ${table}_public_read on public.${table} for select using (true)'; end if; end $$` }),
    });

    const pText = await policyRes.text();
    if (!policyRes.ok && !pText.includes("already exists")) {
      console.warn(`  ⚠ policy ${table}: ${pText.slice(0, 140)}`);
    }

    // Service role bypass
    const srRes = await fetch(mgmtUrl, {
      method: "POST",
      headers: { "Authorization": `Bearer ${mgmtKey}`, "Content-Type": "application/json" },
      body: JSON.stringify({ query: `do $$ begin if not exists (select 1 from pg_policies where schemaname='public' and tablename='${table}' and policyname='${table}_service_write') then execute 'create policy ${table}_service_write on public.${table} for all to service_role using (true) with check (true)'; end if; end $$` }),
    });

    const srText = await srRes.text();
    if (!srRes.ok && !srText.includes("already exists")) {
      console.warn(`  ⚠ service_write policy ${table}: ${srText.slice(0, 140)}`);
    }
  }

  console.log("Migration completed.");
}

applyMigration().catch((err) => { console.error(err); process.exit(1); });
