-- Posting config by electronics subcategory.
with target as (
  select
    ec.id as electronics_category_id,
    ec.category_node_id,
    true as requires_images,
    1 as min_images,
    case
      when ec.slug in ('mobile-phones', 'laptops', 'tablets', 'smart-watches', 'phone-accessories', 'computer-accessories', 'other-electronics') then 8
      else 10
    end as max_images,
    case
      when ec.slug in ('mobile-phones', 'laptops', 'tablets', 'smart-watches', 'second-hand-items') then '3-8'
      when ec.slug in ('phone-accessories', 'computer-accessories') then '1-4'
      else '3-8'
    end as recommended_images,
    true as allow_manual_model,
    false as allow_video
  from public.electronics_categories ec
  where ec.parent_id is not null
    and ec.category_node_id is not null
), updated as (
  update public.posting_category_config cfg
  set
    requires_images = t.requires_images,
    min_images = t.min_images,
    max_images = t.max_images,
    recommended_images = t.recommended_images,
    allow_manual_model = t.allow_manual_model,
    allow_video = t.allow_video,
    category_node_id = coalesce(cfg.category_node_id, t.category_node_id),
    is_active = true
  from target t
  where cfg.electronics_category_id = t.electronics_category_id
     or (cfg.electronics_category_id is null and cfg.category_node_id = t.category_node_id)
  returning cfg.electronics_category_id
)
insert into public.posting_category_config (
  category_node_id,
  electronics_category_id,
  requires_images,
  min_images,
  max_images,
  recommended_images,
  allow_manual_model,
  allow_video,
  is_active
)
select
  t.category_node_id,
  t.electronics_category_id,
  t.requires_images,
  t.min_images,
  t.max_images,
  t.recommended_images,
  t.allow_manual_model,
  t.allow_video,
  true
from target t
left join updated u on u.electronics_category_id = t.electronics_category_id
where u.electronics_category_id is null;

