-- Phones & Electronics complete catalog and posting system

create table if not exists public.electronics_categories (
  id bigint generated by default as identity primary key,
  name text not null,
  parent_id bigint references public.electronics_categories(id) on delete cascade,
  slug text not null unique,
  icon text,
  sort_order int not null default 0,
  is_active boolean not null default true,
  category_node_id bigint references public.category_nodes(id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.electronics_brands (
  id bigint generated by default as identity primary key,
  category_id bigint not null references public.electronics_categories(id) on delete cascade,
  name text not null,
  slug text not null,
  logo_url text,
  sort_order int not null default 0,
  is_popular boolean not null default false,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  unique(category_id, slug)
);

create table if not exists public.electronics_models (
  id bigint generated by default as identity primary key,
  brand_id bigint not null references public.electronics_brands(id) on delete cascade,
  category_id bigint not null references public.electronics_categories(id) on delete cascade,
  name text not null,
  slug text not null,
  release_year int,
  image_url text,
  is_popular boolean not null default false,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  unique(brand_id, slug)
);

create table if not exists public.electronics_model_specs (
  id bigint generated by default as identity primary key,
  model_id bigint not null references public.electronics_models(id) on delete cascade,
  spec_key text not null,
  spec_label text not null,
  spec_value text not null,
  spec_group text,
  is_public boolean not null default true,
  is_filterable boolean not null default false,
  created_at timestamptz not null default now(),
  unique(model_id, spec_key)
);

create table if not exists public.electronics_model_options (
  id bigint generated by default as identity primary key,
  model_id bigint not null references public.electronics_models(id) on delete cascade,
  option_type text not null,
  option_value text not null,
  sort_order int not null default 0,
  created_at timestamptz not null default now(),
  unique(model_id, option_type, option_value)
);

alter table public.posting_category_config
  add column if not exists electronics_category_id bigint references public.electronics_categories(id) on delete cascade,
  add column if not exists allow_manual_model boolean not null default true;

create unique index if not exists ux_posting_category_config_electronics_category_id
  on public.posting_category_config(electronics_category_id)
  where electronics_category_id is not null;

alter table public.category_fields
  alter column category_node_id drop not null;

alter table public.category_fields
  add column if not exists show_condition jsonb;

create table if not exists public.electronics_listings (
  id bigint generated by default as identity primary key,
  listing_id uuid not null unique references public.listings(id) on delete cascade,
  category_id bigint not null references public.electronics_categories(id) on delete restrict,
  brand_id bigint references public.electronics_brands(id) on delete set null,
  model_id bigint references public.electronics_models(id) on delete set null,
  manual_brand text,
  manual_model text,
  condition text,
  price numeric(14,2) not null,
  currency public.currency_code not null default 'AFN',
  storage text,
  ram text,
  color text,
  battery_health text,
  warranty text,
  box_included boolean,
  charger_included boolean,
  repair_history text,
  network_registered text,
  province text,
  district text,
  area text,
  description text,
  created_at timestamptz not null default now()
);

create index if not exists idx_electronics_categories_parent on public.electronics_categories(parent_id, sort_order);
create index if not exists idx_electronics_brands_category on public.electronics_brands(category_id, sort_order);
create index if not exists idx_electronics_models_brand on public.electronics_models(brand_id, is_popular desc, name);
create index if not exists idx_electronics_models_category on public.electronics_models(category_id, is_popular desc, name);
