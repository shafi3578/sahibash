-- categories
create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  slug text not null unique,
  display_order int not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint categories_slug_format check (slug = lower(slug))
);

-- subcategories
create table if not exists public.subcategories (
  id bigint generated by default as identity primary key,
  category_id bigint not null references public.categories(id) on delete cascade,
  name text not null,
  slug text not null,
  display_order int not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint subcategories_unique_name_per_category unique (category_id, name),
  constraint subcategories_unique_slug_per_category unique (category_id, slug),
  constraint subcategories_slug_format check (slug = lower(slug))
);

-- listings
create table if not exists public.listings (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  category_id bigint not null references public.categories(id) on delete restrict,
  subcategory_id bigint not null references public.subcategories(id) on delete restrict,
