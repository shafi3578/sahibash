-- Generic updated_at automation
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- Ensure chosen subcategory belongs to selected category
create or replace function public.validate_listing_category_subcategory()
returns trigger
language plpgsql
as $$
declare
  subcat_category_id bigint;
begin
  select s.category_id into subcat_category_id
  from public.subcategories s
  where s.id = new.subcategory_id;

  if subcat_category_id is null then
    raise exception 'Invalid subcategory_id: %', new.subcategory_id;
  end if;

  if subcat_category_id <> new.category_id then
    raise exception 'subcategory_id % does not belong to category_id %', new.subcategory_id, new.category_id;
  end if;

  return new;
end;
$$;

-- Publish metadata automation on approval status changes
create or replace function public.set_listing_approval_fields()
returns trigger
language plpgsql
as $$
begin
  if new.status = 'approved' and old.status is distinct from 'approved' then
    new.approved_at = coalesce(new.approved_at, now());
    new.published_at = coalesce(new.published_at, now());
  end if;

  if new.status = 'rejected' and old.status is distinct from 'rejected' then
    new.featured = false;
  end if;

  if new.status in ('pending', 'rejected') then
    new.approved_by = null;
    if new.status = 'pending' then
      new.approved_at = null;
      new.published_at = null;
      new.rejection_reason = null;
    end if;
  end if;

  return new;
end;
$$;

-- Keep only one primary image per listing
create or replace function public.ensure_single_primary_image()
returns trigger
language plpgsql
as $$
begin
  if new.is_primary then
    update public.listing_images
      set is_primary = false
    where listing_id = new.listing_id
      and id <> coalesce(new.id, '00000000-0000-0000-0000-000000000000'::uuid)
      and is_primary = true;
  end if;
  return new;
end;
$$;

-- If listing has no primary image yet, first image becomes primary
create or replace function public.assign_first_primary_image()
returns trigger
language plpgsql
as $$
begin
  if not exists (
    select 1
    from public.listing_images li
    where li.listing_id = new.listing_id
      and li.is_primary = true
  ) then
    update public.listing_images
    set is_primary = true
    where id = new.id;
  end if;
  return null;
end;
$$;

-- Increase listing.views_count on every listing_views insert
create or replace function public.increment_listing_view_count()
returns trigger
language plpgsql
as $$
begin
  update public.listings
    set views_count = views_count + 1
  where id = new.listing_id;
  return null;
end;
$$;

-- Auto-create profile after auth signup
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, full_name)
  values (new.id, new.raw_user_meta_data ->> 'full_name')
  on conflict (id) do nothing;
  return new;
end;
$$;

-- Admin helper
create or replace function public.is_admin(uid uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles p
    where p.id = uid
      and p.role = 'admin'
  );
$$;

