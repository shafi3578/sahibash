-- 3) Phone product specs seed
-- =========================================================

with model_specs_seed as (
  select * from (
    values
      ('apple', 'iphone-13-pro-max', array['iphone 13 pro max','apple iphone 13 pro max']::text[]),
      ('apple', 'iphone-14-pro-max', array['iphone 14 pro max','apple iphone 14 pro max']::text[]),
      ('apple', 'iphone-15-pro-max', array['iphone 15 pro max','apple iphone 15 pro max']::text[]),
      ('apple', 'iphone-16-pro-max', array['iphone 16 pro max','apple iphone 16 pro max']::text[]),
      ('samsung', 'galaxy-s24', array['s24','galaxy s24','samsung s24']::text[]),
      ('samsung', 'galaxy-s25', array['s25','galaxy s25','samsung s25']::text[]),
      ('xiaomi', 'redmi-note-13', array['redmi note 13','xiaomi redmi note 13']::text[]),
      ('xiaomi', 'poco-x6', array['poco x6','xiaomi poco x6']::text[]),
      ('huawei', 'p50', array['huawei p50','p50 huawei']::text[]),
      ('honor', 'honor-90', array['honor 90']::text[])
  ) as t(brand_slug, model_slug, aliases)
), all_model_nodes as (
  select
    model.id as category_node_id,
    brand.name as brand,
    model.name as model,
    brand.slug as brand_slug,
    model.slug as model_slug,
    model.path
  from public.category_nodes model
  join public.category_nodes brand on brand.id = model.parent_id
  where model.path like 'phones-electronics/mobile-phones/%/%'
    and model.level = 4
), final_specs as (
  select
    n.category_node_id,
    n.brand,
    n.model,
    coalesce(s.aliases, array[lower(n.model), regexp_replace(lower(n.model), '[^a-z0-9]+', '', 'g')]::text[]) as aliases,
    jsonb_build_object(
      'brand', n.brand,
      'model', n.model,
      'operating_system', case when n.brand_slug = 'apple' then 'iOS' else 'Android' end,
      'screen_size', case
        when n.model_slug like '%pro-max' then '6.7 inch'
        when n.model_slug like '%plus' then '6.7 inch'
        when n.model_slug like '%mini' then '5.4 inch'
        when n.model_slug in ('iphone-6','iphone-6s','iphone-7','iphone-8') then '4.7 inch'
        when n.model_slug in ('iphone-x','iphone-xr','iphone-xs') then '6.1 inch'
        when n.model_slug like 'galaxy-note-%' then '6.7 inch'
        else '6.5 inch'
      end,
      'storage_options', case
        when n.brand_slug = 'apple' then '["64 GB","128 GB","256 GB","512 GB","1 TB"]'::jsonb
        when n.brand_slug = 'samsung' then '["64 GB","128 GB","256 GB","512 GB"]'::jsonb
        when n.brand_slug = 'xiaomi' then '["64 GB","128 GB","256 GB"]'::jsonb
        else '["64 GB","128 GB","256 GB"]'::jsonb
      end,
      'ram_options', case
        when n.brand_slug = 'apple' then '["4 GB","6 GB","8 GB"]'::jsonb
        when n.brand_slug = 'samsung' then '["4 GB","6 GB","8 GB","12 GB"]'::jsonb
        when n.brand_slug = 'xiaomi' then '["4 GB","6 GB","8 GB","12 GB"]'::jsonb
        else '["4 GB","6 GB","8 GB"]'::jsonb
      end,
      'camera_info', case
        when n.model_slug = 'iphone-13-pro-max' then '12 MP'
        when n.model_slug like '%pro%' then '48 MP'
        when n.model_slug like 'galaxy-s2%' then '50 MP'
        else '48 MP'
      end,
      'front_camera', case
        when n.model_slug like 'iphone-%' then '12 MP'
        else '16 MP'
      end,
      'sim_type', case
        when n.brand_slug = 'apple' then 'Nano-SIM and eSIM'
        else 'Dual SIM'
      end
    ) as specs
  from all_model_nodes n
  left join model_specs_seed s
    on s.brand_slug = n.brand_slug
   and s.model_slug = n.model_slug
)
insert into public.product_specs (category_node_id, brand, model, aliases, specs, is_active)
select
  f.category_node_id,
  f.brand,
  f.model,
  f.aliases,
  f.specs,
  true
from final_specs f
on conflict (category_node_id, brand, model) do update
set
  aliases = excluded.aliases,
  specs = excluded.specs,
  is_active = true;

