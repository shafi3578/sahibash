-- =========================================================
-- 1) Schema additions
-- =========================================================

create table if not exists public.product_specs (
  id bigint generated by default as identity primary key,
  category_node_id bigint not null references public.category_nodes(id) on delete cascade,
  brand text not null,
  model text not null,
  aliases text[] not null default '{}'::text[],
  specs jsonb not null default '{}'::jsonb,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  constraint product_specs_unique_node_brand_model unique (category_node_id, brand, model)
);

create table if not exists public.ai_detection_logs (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  image_urls text[] not null default '{}'::text[],
  title text,
  description text,
  detected_labels jsonb not null default '[]'::jsonb,
  suggested_category_node_id bigint references public.category_nodes(id) on delete set null,
  suggested_specs jsonb not null default '{}'::jsonb,
  confidence numeric(5,4),
  created_at timestamptz not null default now()
);

create index if not exists idx_product_specs_category_node_id on public.product_specs(category_node_id);
create index if not exists idx_product_specs_brand_model on public.product_specs(brand, model);
create index if not exists idx_product_specs_aliases_gin on public.product_specs using gin(aliases);
create index if not exists idx_product_specs_specs_gin on public.product_specs using gin(specs);

create index if not exists idx_ai_detection_logs_user_id_created_at on public.ai_detection_logs(user_id, created_at desc);
create index if not exists idx_ai_detection_logs_suggested_category_node_id on public.ai_detection_logs(suggested_category_node_id);

alter table public.product_specs enable row level security;
alter table public.ai_detection_logs enable row level security;

drop policy if exists product_specs_public_read on public.product_specs;
create policy product_specs_public_read
on public.product_specs
for select
using (is_active = true);

drop policy if exists product_specs_admin_manage on public.product_specs;
create policy product_specs_admin_manage
on public.product_specs
for all
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

drop policy if exists ai_detection_logs_owner_insert on public.ai_detection_logs;
create policy ai_detection_logs_owner_insert
on public.ai_detection_logs
for insert
to authenticated
with check (user_id = auth.uid());

drop policy if exists ai_detection_logs_owner_select on public.ai_detection_logs;
create policy ai_detection_logs_owner_select
on public.ai_detection_logs
for select
to authenticated
using (user_id = auth.uid() or public.is_admin(auth.uid()));

